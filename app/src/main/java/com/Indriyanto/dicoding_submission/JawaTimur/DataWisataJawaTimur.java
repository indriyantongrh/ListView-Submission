    static ArrayList<JawaTimur> getListData() {
        ArrayList<JawaTimur> list = new ArrayList<>();
        for (int position = 0; position < namaWisata.length; position++) {
            JawaTimur jatim = new JawaTimur();
            jatim.setNamaWisata(namaWisata[position]);
            jatim.setKategori(kategoriWisata[position]);
            jatim.setAlamat(alamatWisata[position]);
            jatim.setGambar(wisataImages[position]);
            jatim.setTiketmasuk(tiketWisata[position]);
            jatim.setDeskripsi(wisataDetails[position]);
            list.add(jatim);
        }
        return list;
    }

